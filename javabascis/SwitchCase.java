package javabascis;

public class SwitchCase {

	public static void main(String[] args) {
		switch(2)
		{
		case 1:
		{
			System.out.println("Logical 1");
		}
		
		case 2:
		{
			System.out.println("Logical 1");
			break;
		}
		
		case 3:
		{
			System.out.println("Logical 1");
		}
		
		case 4:
		{
			System.out.println("Logical 1");
		}
		default:
			System.out.println("Sorry you made a worng selection");
	}

}
}
